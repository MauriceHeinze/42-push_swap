#include "../includes/push_swap.h"

void	ft_subarr(long **src, long **dst, long index, long size)
{
	long	i;

	i = 0;
	while (i < size)
	{
		(*dst)[i] = (*src)[index + i];
		i++;
	}
	(*dst)[i] = LONG_MAX;
}
