#include "../includes/push_swap.h"

void	ft_print_array(long stack[])
{
	long	i;

	i = 0;
	while (i < ft_stacklen(&(*stack)))
	{
		printf("%ld, ", stack[i]);
		i++;
	}
	printf("%ld", stack[i]);
	printf("\n");
}
